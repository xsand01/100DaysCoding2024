package Coding100Day;

public class Day_85 {

    //Mengisi elemen ke Array satu dimensi pada saat di inisialisasi 
    public static void main(String[] args) {
        int[] a = {1, 2, 3, 4, 5};
        for (int i = 0; i <= a.length; i++) {
            System.out.println("index ke - " + i + " = " + a[i]);
        }
    }
}
