// Day 8 = Variabel Update

         // >>Variabel Awal<<
public class Day8 {
    public static void main(String[] args) {
        int Umur = 18;
        System.out.println("Umur : " + Umur + "Tahun");
        
        //>>Variabel  After Update<<
        Umur =  20;
        System.out.println("Umur : " + Umur + " Tahun");
       
    }
  
}
